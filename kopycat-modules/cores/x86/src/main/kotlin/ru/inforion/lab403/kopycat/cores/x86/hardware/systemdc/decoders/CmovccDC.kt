class CmovccDC(core: x86Core, val construct: (x86Core, ByteArray, Prefixes, AOperand<x86Core>, AOperand<x86Core>) -> AX86Instruction) :
        ADecoder<AX86Instruction>(core) {

    override fun decode(s: x86OperandStream, prefs: Prefixes): AX86Instruction {
        val opcode = s.last
        val ops = when (opcode) {
            0x0F -> {
                val sopcode = s.readByte().int
                val rm = RMDC(s, prefs)
                when (sopcode) {
                    0x40, 0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47,
                    0x48, 0x49, 0x4A, 0x4B, 0x4C, 0x4D, 0x4E, 0x4F -> arrayOf(rm.rpref, rm.mpref)
                    else -> throw GeneralException("Incorrect opcode in decoder")
                }
            }
            else -> throw GeneralException("Incorrect opcode in decoder")
        }
        return construct(core, s.data, prefs, ops[0], ops[1])
    }
}